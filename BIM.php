Bachelor in Information Management (BIM) is a four–year academic program offered by Tribhuvan University (TU).  This program is a blend of information technology (40%) and management (60%). This program aims at helping students develop skills in information technology and management giving a career choice both in IT and management. The main objective of this program is to develop socially responsible, scientifically approached and result–oriented information technology (IT) professionals with some management skills.
</h2>
<h1 style="font-size:60px;color:red;font-family:arial;text-align:left;">Objectives</h1>
<h2>The specific objectives of the program are to:</h2>
<h3>
Prepare IT professionals proficient in the use of computers and computational techniques in order to develop effective information systems to solve real life problems in the organizational milieu.
Develop students’ skills in object–oriented software design methods and database management systems.
Provide professional training to students by combining information technology with managerial skills.
Prepare students to proceed on to post-graduate level study in information management within and outside the country.</h3><br><br>
<h1 style="font-size:60px;color:red;font-family:arial;text-align:left;">Scope and Career Prospects</h1>
<h2>Graduating with Bachelor in Information Management (BIM) is highly beneficial as it opens up an diverse career options for students. This is mainly due to the fact that employers now–a–days show a growing interest for applicants with both business management education as well as an IT background. The combination of IT and management that BIM degree offers has career prospects such as Software Designers & Engineers, Advertising & Marketing Executives, IT Consultants & Planners, Sales & Retail Assistants, Computer Programmers & Developer, Middle Level Managers/Officers, Network Managers/ Officers, Network Managers/ System Analysts, Software Programmers/Engineers, System Administrators, MIS/ IT/ Computer Officers, Software Project Managers, Entrepreneurs, IT Faculty, etc.</h2>
