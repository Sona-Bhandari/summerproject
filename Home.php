<H1 style="font-size:60px;color:blue;font-family:arial;text-align:left;">Introduction</H1>
<H2>NIST College was established in 2012 AD as an expansion of NIST National School of Sciences, Kathmandu. The College has worked its way up from a humble beginning with very few students and teaching staff to become a premier academic institution in a short span of time due to its consistent emphasis on quality education. Situated in a serene and secluded area on the side of Nala Road, Naya Basti, in the north of Banepa, just 1.5 kilometers from the main city, it has its own academic buildings with ample space creating a stimulating academic environment. The prime objective behind the establishment of NIST College has been to provide students quality education in the region so that they do not have to travel to the capital city that is more congested, noisy and relatively expensive.</H2>

